// A function with two arguments

var showQuotient;

showQuotient = function (number1, number2) {
  var result = number1 / number2;
  console.log("The quotient is " + result);
};

showQuotient(10, 2);  
showQuotient(15, 3);   
showQuotient(8, 2.5);  

